# Напишите программу, которая получает на вход строку
# с названием текстового файла и выводит на экран
# содержимое этого файла, заменяя все запрещённые слова
# звездочками

import re  # Модуль для поиска и замены по шаблонам

# если нужен любой файл, то нкжно снять коммент и ввести название файла
# filename = input("Введите имя файла для цензуры: ")

#  Открываем файл со стоп-словами
with open("stop_words.txt", "r", encoding="utf-8") as f:
    stop_words = f.read().split()

#  Открываем текст, который нужно цензурировать, если выбран руччной ввод закомментить
with open("text.txt", "r", encoding="utf-8") as f:
    text = f.read()

# это если разблокирован ручной ввод файла
# with open(filename, "r", encoding="utf-8") as f:
#     text = f.read()

#  Для каждого запрещённого слова заменяем ВСЕ вхождения (независимо от регистра)
for word in stop_words:
    # (?i) — это флаг "не учитывать регистр" прямо в шаблоне
    # re.escape(word) — чтобы если в слове есть спецсимволы, они не поломали поиск
    pattern = re.compile(r'(?i)' + re.escape(word))
    text = pattern.sub('*' * len(word), text)

# 5. Печатаем результат
print("\n=== Текст после цензуры ===")
print(text)
